public class Array_1 {
  public static void main(String[] args) {

    // 배열의 선언 및 생성

    // 정수를 여러개 저장할 수 있는 배열을 선언
    // 자료형 변수명;
    int[] arr1;

    // 배열의 생성(~~초기화)
    arr1 = new int[5]; // 5개의 정수를 저장할 수 있는 배열 arr1 생성

    double[] arr2; // 실수를 여려개 저장할 수 있는 배열 arr2 선언
    arr2 = new double[3];  // 3개의 실수를 저장할 수 있는 배열 arr2 생성

    // 문자열 5개를 저장할 수 있는 배열 arr3 선언 및 생성
    String[] arr3 = new String[5];

    // 생성 시 초기화 값  ->  정수 : 0, 실수 : 0.0, 문자열 : null
    // null : 정해진 데이터가 없다. 비어있다. (null은 문자열이 아니다.)
    // 기본자료형을 제외한 모든 배열은 생성 시 'null'로 초기화





  }
}
